class variables{

    short s;
    int i;
    long l;
    float f;
    double d;
    char c;
    boolean b;

    public static void main(String[] args) {
        variables v = new variables();

        System.out.print("short : "+v.s+'\n'+"int : "+v.i+'\n'+"long : "+v.l+'\n'+"float : "+v.f+'\n'+"double : "+v.d+'\n'+"char : "+v.c+'\n'+"boolean : "+v.b+'\n');
    }
}